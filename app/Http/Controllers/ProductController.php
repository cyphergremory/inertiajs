    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $products = Product::paginate();

        return Inertia::render('Products/Index',[
            'products' => $products->items(),
            'links' => $products->links()
        ]);
    }

